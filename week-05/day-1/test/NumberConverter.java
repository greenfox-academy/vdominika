public class NumberConverter {


    public String numberToWord(int number) {

       if (number == 5 ) {
           return "five";
       } else if (number == 4) {
           return "four";
       } else if (number == 3) {
           return "three";
       } else if (number == 2) {
           return "two";
       } else {
           return "one";
        }

    }
}
